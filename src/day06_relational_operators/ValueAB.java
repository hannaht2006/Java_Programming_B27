package day06_relational_operators;

public class ValueAB {
  /*

    What is the value of a and b


    int a = 8;
    int b = a--;

    what is a? 7
    what is b? 8
     */
  public static void main(String[] args) {
      int a =8;
      int b = a--;
      System.out.println(a);//7
      System.out.println(b);//8
  }





}
